# 기본 계산기
def add(a, b):
    return a+b


def subtract(a, b):
    return a-b


def multiply(a, b):
    return a*b


def diveide_new(a, b):
    return a/b


def get_Abs(num):
    if num >= 0:
        return num
    else:
        return -num


def getPercent(a, b):
    return (a/b) * 100


def getMedian(a, b):
    return (a+b)/2


def getSum_ver1(n):
    return n(n+1)/2
